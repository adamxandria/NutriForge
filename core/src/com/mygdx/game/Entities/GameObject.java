package com.mygdx.game.Entities;


public class GameObject {

    protected float x;
    protected float y;

    public float getX() {
        return x;
    }
    public float getY() {
        return y;
    }
    public void setX(float x) {
        this.x = x;
    }
    public void setY(float y) {
        this.y = y;
    }

}
